package java.awt.geom;

import java.awt.*;

public class RectangularShape implements Shape {
    public int x;
    public int y;
    public int width;
    public int height;

    public RectangularShape(int x, int y, int w, int h) {
        this.x = x;
        this.y = y;
        this.width = w;
        this.height = h;
    }

    public double getWidth() {
        return width;
    }

    public double getHeight() {
        return height;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
